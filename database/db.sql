  UNI_LOCATIVA VARCHAR(255) NOT NULL,
  EMPRESA VARCHAR(255) NOT NULL,
  TIPO_SERVICIO VARCHAR(255) NOT NULL,
  NRO_CONTRATO VARCHAR(255) NOT NULL,
  NRO_LECTURA VARCHAR(255) NOT NULL,
  NRO_CLIENTE VARCHAR(255) NOT NULL,
  NRO_MEDIDOR VARCHAR(255) NOT NULL,
  FOTO VARCHAR(255) NOT NULL,

  MONTO  INT NOT NULL,
  /*NRO_ INT NOT NULL,*/
  PRIMARY KEY (id)
