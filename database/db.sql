CREATE DATABASE SERVICIOSDB

CREATE TABLE SERVICIOS (
  ID SERIAL,
  CENTRO_COMERCIAL VARCHAR(255) NOT NULL,
  OPERADOR VARCHAR(255) NOT NULL,
  NRO_CONTRATO VARCHAR(255) NOT NULL,
  SUCURSAL VARCHAR(255) NOT NULL,
  OPM_MEDIDOR VARCHAR(255) NOT NULL,
  TARIFA VARCHAR(255) NOT NULL,
  TIPO_MEDIDOR VARCHAR(255) NOT NULL,
  ESTADO VARCHAR(255) NOT NULL,
  PERIODO VARCHAR(255) NOT NULL,
  /*NRO_ INT NOT NULL,*/
  PRIMARY KEY (id)
);

CREATE TABLE ACTIVOS (
  ID SERIAL,
  NOMBRE VARCHAR(255) NOT NULL,
  ETIQUETA VARCHAR(255) NOT NULL,
  EMPRESA VARCHAR(255) NOT NULL,
  RESPONSABLE VARCHAR(255) NOT NULL,
  EDIFICIO VARCHAR(255) NOT NULL,
  PISO VARCHAR(255) NOT NULL,
  TIPO_ACTIVO VARCHAR(255) NOT NULL,
  SUBTIPO_ACTIVO VARCHAR(255) NOT NULL,
  SUCURSAL VARCHAR(255) NOT NULL,
  NRO_CONTRATO  VARCHAR(255) NOT NULL,
  RUT VARCHAR(255) NOT NULL,
  FECHA_INICIO VARCHAR(255) NOT NULL,
  /*NRO_ INT NOT NULL,*/
  PRIMARY KEY (id)
);

CREATE TABLE REGISTRO_CONSUMO_ISF (
